def preprocess(text):
    text = re.sub('[^가-힣]', ' ', text)
    tokens = okt.pos(text, stem=True)
    words = [w for w, p in tokens
             if p in ['Noun', 'Verb', 'Adjective'] and len(w) > 1 and w not in stopwords]
    return ' '.join(words)


